export const hello = (name: string) => {
  return `Hello ${name}`;
}
export const goodbye = (name: string) => {
  return `Goodbye ${name}`;
}
export const greet = (name: string) => {
  return `Greetings ${name}`;
}
export const farewell = (name: string) => {
  return `Farewell ${name}`;
}
export const welcome = (name: string) => {
  return `Welcome ${name}`;
}